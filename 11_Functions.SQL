--FUNCTIONS IN SQL

--STRING FUNCTIONS
-- CONCAT
SELECT CONCAT('Hello', ' World') AS RESULT FROM DUAL;

-- INSTR
SELECT INSTR('DATABASE', 'BASE') AS POSITION FROM DUAL;

-- UPPER
SELECT UPPER('nainsi') AS RESULT FROM DUAL;

-- LOWER
SELECT LOWER('MUMBAI') AS RESULT FROM DUAL;

-- SUBSTR
SELECT SUBSTR('DATABASE', 1, 4) AS RESULT FROM DUAL;

-- REPLACE
SELECT REPLACE('HELLO WORLD', 'WORLD', 'SQL') AS RESULT FROM DUAL;

-- LENGTH
SELECT LENGTH('DATABASE') AS RESULT FROM DUAL;

-- TRIM
SELECT TRIM('   HELLO   ') AS RESULT FROM DUAL;

-- LTRIM
SELECT LTRIM('   HELLO') AS RESULT FROM DUAL;

-- RTRIM
SELECT RTRIM('HELLO   ') AS RESULT FROM DUAL;




--MATH FUNCTION
-- ABS
SELECT ABS(-10) AS RESULT FROM DUAL;

-- CEIL
SELECT CEIL(4.2) AS RESULT FROM DUAL;

-- FLOOR
SELECT FLOOR(4.8) AS RESULT FROM DUAL;

-- MOD
SELECT MOD(10, 3) AS RESULT FROM DUAL;

-- POWER
SELECT POWER(2, 3) AS RESULT FROM DUAL;

-- SQRT
SELECT SQRT(16) AS RESULT FROM DUAL;

-- ROUND
SELECT ROUND(45.678, 2) AS RESULT FROM DUAL;

-- TRUNCATE
SELECT TRUNC(45.678, 2) AS RESULT FROM DUAL;




--DATE FUNCTION
-- SYSDATE
SELECT SYSDATE FROM DUAL;

-- CURRENT_DATE
SELECT CURRENT_DATE FROM DUAL;

-- ADD_MONTHS
SELECT ADD_MONTHS(SYSDATE, 3) AS RESULT FROM DUAL;

-- EXTRACT
SELECT EXTRACT(YEAR FROM SYSDATE) AS YEAR FROM DUAL;

-- TO_DATE
SELECT TO_DATE('2025-05-03', 'YYYY-MM-DD') AS RESULT FROM DUAL;

-- DATE DIFFERENCE
SELECT SYSDATE - TO_DATE('2025-01-01', 'YYYY-MM-DD') AS DAYS_DIFF FROM DUAL;




